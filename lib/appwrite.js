export const getFilePreview = async (fileId, type) => {
  let fileUrl;

  try {
    if (type === "video") {
      fileUrl = storage.getFileView(config.storageId, fileId);
    } else if (type === "image") {
      fileUrl = storage.getFilePreview(
        config.storageId,
        fileId,
        2000,
        2000,
        "top",
        100
      );
    } else {
      throw new Error("Invalid file type");
    }
    if (!fileUrl) throw Error;
    return fileUrl;
  } catch (error) {
    throw new Error(error);
  }
};
export const uploadFile = async (file, type) => {
  if (!file) return;
  const { mimeType, ...rest } = file;
  const asset = { type: mimeType, ...rest };
  try {
    const uploadedFile = await storage.createFile(
      config.storageId,
      ID.unique(),
      asset
    );
    const fileUrl = await getFilePreview(uploadedFile.$id, type);
    return fileUrl;
  } catch (error) {
    throw new Error(error);
  }
};
export const createVideo = async (form) => {
  try {
    const [thumbnailUrl, videoUrl] = await Promise.all([
      uploadFile(form.thumbnail, "image"),
      uploadFile(form.video, "video"),
    ]);
    const newPost = await databases.createDocument(
      config.databaseId,
      config.videoCollectionId,
      ID.unique(),
      {
        title: form.title,
        thumbnail: thumbnailUrl,
        video: videoUrl,
        prompt: form.prompt,
        creator: form.userId,
      }
    );
    return newPost;
  } catch (error) {
    throw new Error(error);
  }
};
export const saveVideo = async (videoId , userId) => {
  console.log('save video function')
  try {
    const video = await databases.getDocument(config.databaseId,config.videoCollectionId,videoId);
    const currentSavers = video.saver || [];
    const userIds = currentSavers.map((saver) => saver.$id);

    if (!userIds.includes(userId)) {
      console.log('first save')
      const updatedVideo = await databases.updateDocument(
        config.databaseId,
        config.videoCollectionId,
        videoId,
        {
          saver: [...currentSavers, userId], 
        }
      );
      console.log("Video bookmarked successfully", updatedVideo);
    } else {
      console.log("User has already bookmarked this video.");
    }
  } catch (error) {
    console.error("Error bookmarking the video:", error);
  }
};

